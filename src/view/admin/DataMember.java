    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        int row = tabel_data_member.getSelectedRow();
        String nama = tabel_data_member.getValueAt(row, 1).toString();
        String alamat = tabel_data_member.getValueAt(row, 2).toString();

        if (nama.isEmpty() || alamat.isEmpty()) {

        } else {
            Member m = new Member();
            int id = m.getIdUser(nama, alamat);
            if (id != 0) {
                String data[][] = m.getProfilById(id);
                edt_nama.setText(data[0][0]);
                edt_alamat.setText(data[0][1]);
                //set to combo jenis
                int idJenis = m.getIDJenisMember(data[0][2]);
//                System.out.println(""+idJenis);
                String[] jMember = m.getListJenisMemberById(idJenis);
                if (jMember != null) {
                    comboJenisMember.removeAllElements();
                    comboJenisMember.addElement(jMember[0]);
                    combo_jenis_member.setModel(comboJenisMember);
                }
            }
        }
    }//GEN-LAST:event_btn_editActionPerformed

