    //set to combo member
    private void setDataCombo(Member m) {
        comboJenisMember.removeAllElements();
        String listMember[] = m.getListJenisMember();
        if (listMember.length > 0) {
            for (int i = 0; i < listMember.length; i++) {
                comboJenisMember.addElement(listMember[i]);
            }
            combo_jenis_member.setModel(comboJenisMember);
        }
    }

    //set data table member
    private void setDataTable(Member m) {
        tabel.getDataVector().removeAllElements();
        String data[][] = m.getDataMember();
        if (data.length > 0) {
            for (int i = 0; i < data.length; i++) {
                int no = i + 1;
                tabel.addRow(new Object[]{no, data[i][0], data[i][1], data[i][2]});
            }
            tabel_data_member.setModel(tabel);
        }
    }


