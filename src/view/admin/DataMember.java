    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        String nama = edt_nama.getText();
        String alamat = edt_alamat.getText();
        String arrJenisMember[] = combo_jenis_member.getSelectedItem().toString().split("-");
        int idJenisMember = 0;
        idJenisMember = Integer.parseInt(arrJenisMember[0]);

        if (nama.isEmpty() || alamat.isEmpty() || idJenisMember == 0) {
            JOptionPane.showMessageDialog(this, "Field tidak boleh kosong");
        } else {
            Member m = new Member();
            int isCek = m.cekUser(nama, alamat);
            if (isCek == 0) {
                m.insertDataMember(nama, alamat, idJenisMember);
                setDataTable(m);
                edt_nama.setText("");
                edt_alamat.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Data sudah ada");
            }
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

