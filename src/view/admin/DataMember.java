    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        Member m = new Member();
        int row = tabel_data_member.getSelectedRow();
        String nama = tabel_data_member.getValueAt(row, 1).toString();
        String alamat = tabel_data_member.getValueAt(row, 2).toString();
        int id = m.getIdUser(nama, alamat);

        if (id != 0) {
            m.deleteData(id);
            setDataTable(m);
        }
        tabel.fireTableDataChanged();
    }//GEN-LAST:event_btn_hapusActionPerformed

